lang = 'PYTHON'
print(lang) # PYTHON
print(lang[0]) # P 인덱스 0 (0부터 시작)
print(lang[-1]) # N 인덱스 -1 (끝에서 1번째)
print(lang[:3]) # PYT 인덱스 0부터 2까지
print(lang[2:]) # THON 인덱스 2부터 끝까지
print(lang[:]) # PYTHON 인덱스 0부터 끝까지

